//--------------------------- 재고추가 메소드 ---------------------------

void 재고추가 ( int menu_select2, int add ) {
	
	if( menu_select2 == 1 ) { // 1번 아메리카노 추가
		아메리카노_재고 += add;
	}
	
	else if ( menu_select2 == 2 ) { // 2번 바닐라라떼 추가
		바닐라라떼_재고 += add;
	}
	
	else if ( menu_select2 == 3 ) { // 3번 카페라떼 추가
		카페라떼_재고 += add;	
	}
		
	else if ( menu_select2 == 4 ) { // 4번 카푸치노 추가
		카푸치노_재고 += add;
	}
		
	else if ( menu_select2 == 5 ) { // 5번 그린티라떼 추가
		그린티라떼_재고 += add;
	}
	

} // 메소드 종료



















	
	
	
