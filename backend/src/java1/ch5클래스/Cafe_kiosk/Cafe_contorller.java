//--------------------------- 결제 메소드 ---------------------------

public void 결제( int 결제금액 ) {
	총매출금액 += 총금액;
	if( 결제금액 == 총금액 ) {
		if(아메리카노_구매수 > 0 ) {
			아메리카노_매출량 += 아메리카노_구매수;
		}
		if(바닐라라떼_구매수 > 0 ) {
			바닐라라떼_매출량 += 바닐라라떼_구매수;
		}
		if(카페라떼_구매수 > 0 ) {
			카페라떼_매출량 += 카페라떼_구매수;
		}
		if(카푸치노_구매수 > 0 ) {
			카페라떼_매출량 += 카푸치노_구매수;
		}
		if(그린티라떼_구매수 > 0 ) {
			그린티라떼_매출량 += 그린티라떼_구매수;
		}
		System.out.println(" [안내] 결제가 완료되었습니다. ");
		총금액 = 0;
		아메리카노_구매수 = 0;
		바닐라라떼_구매수 = 0;
		카페라떼_구매수 = 0;
		카푸치노_구매수 = 0;
		그린티라떼_구매수 = 0;

		return;
	} // if 종료
	
	else if ( 결제금액 > 총금액 ) {
		System.out.println(" [안내] 잔돈을 반환 받으세요. ");
		System.out.println( " " + (결제금액 - 총금액) + "원");
		
		if(아메리카노_구매수 > 0 ) {
			아메리카노_매출량++;
		}
		if(바닐라라떼_구매수 > 0 ) {
			바닐라라떼_매출량++;
		}
		if(카페라떼_구매수 > 0 ) {
			카페라떼_매출량++;
		}
		if(카푸치노_구매수 > 0 ) {
			카페라떼_매출량++;
		}
		if(그린티라떼_구매수 > 0 ) {
			그린티라떼_매출량++;
		}
		
		System.out.println(" [안내] 결제가 완료되었습니다. ");
		총금액 = 0;
		아메리카노_구매수 = 0;
		바닐라라떼_구매수 = 0;
		카페라떼_구매수 = 0;
		카푸치노_구매수 = 0;
		그린티라떼_구매수 = 0;
		
		return;
	} // if 종료
	
	else if ( 결제금액 < 총금액 ) {
		System.out.println(" [안내] 금액이 부족합니다 : 결제취소 ");
		return;
	}
} // 메소드 종료

